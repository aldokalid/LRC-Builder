    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if (s != null) {
            if (s.getTheme() != jComboBox1.getSelectedIndex()) {
                JOptionPane.showMessageDialog(this, "Restart the program to apply theme.",
                        "Theme changed.", JOptionPane.INFORMATION_MESSAGE);
            }

            s.setTheme(jComboBox1.getSelectedIndex());
            s.setDelay(Time.splitTime(delayTxt.getText()));
            s.setAddEmptyLines(empLinChBx.isSelected());
            s.setSaveTags(tagChBx.isSelected());

            new DAOIO<Setting>(new Global().getFilePath(Global.SETTING_FILE)).write(s);
            this.dispose();
        }
    }//GEN-LAST:event_formWindowClosing

